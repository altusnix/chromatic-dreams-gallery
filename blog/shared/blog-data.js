// Helper functions for working with blog data
export function getPostById(id) {
  return blogPosts.find(post => post.id === id);
}

export function getPostBySlug(slug) {
  return blogPosts.find(post => post.slug === slug);
}

export function getFeaturedPosts() {
  return blogPosts.filter(post => post.featured);
}

export function getPostsByCategory(category) {
  return blogPosts.filter(post => post.category === category);
}

export function getAllCategories() {
  const categories = [...new Set(blogPosts.map(post => post.category))];
  return categories.sort();
}

export function getPostsByTag(tag) {
  return blogPosts.filter(post => post.tags.includes(tag));
}

export function getNextPost(currentId) {
  const currentIndex = blogPosts.findIndex(post => post.id === currentId);
  return currentIndex < blogPosts.length - 1 ? blogPosts[currentIndex + 1] : null;
}

export function getPreviousPost(currentId) {
  const currentIndex = blogPosts.findIndex(post => post.id === currentId);
  return currentIndex > 0 ? blogPosts[currentIndex - 1] : null;
}

export function getRecentPosts(limit = 6) {
  return [...blogPosts]
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
}

export function getRelatedPosts(currentPost, limit = 3) {
  const related = blogPosts
    .filter(post => post.id !== currentPost.id)
    .filter(post =>
      // Find posts with matching tags or category
      post.category === currentPost.category ||
      post.tags.some(tag => currentPost.tags.includes(tag))
    )
    .slice(0, limit);

  // If we don't have enough related posts, fill with recent posts
  if (related.length < limit) {
    const additional = blogPosts
      .filter(post => post.id !== currentPost.id)
      .filter(post => !related.includes(post))
      .slice(0, limit - related.length);
    related.push(...additional);
  }

  return related;
}